    private void calculateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateButtonActionPerformed
        DefaultTableModel myTable = (DefaultTableModel) jTable1.getModel();

        int row_count = myTable.getRowCount();
        for (int i = 0; i < row_count; i++) {
            RecIntegral recIntegral = recIntegrals.get(i);
            try{
                 recIntegral.run();
                 recIntegral.join();
            }catch(Exception ex){
                 ex.printStackTrace();
            }
            
            if (recIntegral.GetResult().obj != null) {
                myTable.setValueAt(recIntegral.GetResult().obj, i, 3);
            } else {
                myTable.setValueAt(recIntegral.GetResult().str, i, 3);
            }
        }
    }//GEN-LAST:event_calculateButtonActionPerformed

