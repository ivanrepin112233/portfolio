    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        RecIntegral recIntegral;

        try {
            float bottomBorder = Float.parseFloat(bottomBorderField.getText());
            float upperBorder = Float.parseFloat(upperBorderField.getText());
            float step = Float.parseFloat(stepField.getText());

            recIntegral = new RecIntegral(bottomBorder, upperBorder, step);

        } catch (InException e) {
            JFrame jFrame = new JFrame();
            JOptionPane.showMessageDialog(jFrame, "Некоректные данные! Введите другие!");
            return;
        }

        recIntegrals.add(recIntegral);
        DefaultTableModel myTable = (DefaultTableModel) jTable1.getModel();
        myTable.addRow(new Object[]{recIntegral.GetBottomBorder(), recIntegral.GetUpperBorder(), recIntegral.GetStep()});
    }//GEN-LAST:event_addButtonActionPerformed

    private void saveInTextTypeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveInTextTypeButtonActionPerformed
        JFileChooser fileopen = new JFileChooser();
        int ret = fileopen.showDialog(null, "Открыть файл");
        if (ret == JFileChooser.APPROVE_OPTION) {
            File file = fileopen.getSelectedFile();
            try {
                FileOutputStream outputStream = new FileOutputStream(file);
                outputStream.close();
                ObjectOutputStream out = new ObjectOutputStream(new FileOutputStream(file));
                out.writeObject(recIntegrals);

            } catch (IOException ex) {
                ex.printStackTrace();
            }

        }
    }//GEN-LAST:event_saveInTextTypeButtonActionPerformed

    private void loadInTextTypeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadInTextTypeButtonActionPerformed
        JFileChooser fileopen = new JFileChooser();
        int ret = fileopen.showDialog(null, "Открыть файл");
        if (ret == JFileChooser.APPROVE_OPTION) {
            File file = fileopen.getSelectedFile();

            try {
                ObjectInputStream in = new ObjectInputStream(new FileInputStream(file));

                recIntegrals = (ArrayList<RecIntegral>) in.readObject();

                updateTable();

            } catch (Exception ex) {
                ex.printStackTrace();
            }

        }

    }//GEN-LAST:event_loadInTextTypeButtonActionPerformed

    private void updateTable() {
        DefaultTableModel myTable = (DefaultTableModel) jTable1.getModel();
        for (RecIntegral recIntegral : recIntegrals) {
            float bottomBorder = recIntegral.GetBottomBorder();
            float upperBorder = recIntegral.GetUpperBorder();
            float step = recIntegral.GetStep();
            if (recIntegral.GetResult().obj != null) {
                myTable.addRow(new Object[]{bottomBorder, upperBorder, step, recIntegral.GetResult().obj});
            } else {
                myTable.addRow(new Object[]{bottomBorder, upperBorder, step, recIntegral.GetResult().str});
            }
        }
    }

